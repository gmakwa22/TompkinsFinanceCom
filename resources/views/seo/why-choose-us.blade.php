            });
            
            // Add click effect
            item.addEventListener('click', function() {
                this.style.transform = 'translateY(-1px) scale(0.99)';
                setTimeout(() => {
                    this.style.transform = 'translateY(-3px) scale(1.01)';
                }, 150);
            });
